
#' multiply
#'
#' multiply numeric input by seven
#' @param A numeric input 'x'
#' @keywords seven
#' @export
#' @examples
#' multiply()

multiply <- function(x) {
  y <- 7 * x
  print("and the answer is...")
  print(y)
}

#' divide
#'
#' divide numeric input by seven
#' @param A numeric input 'x'
#' @keywords seven
#' @export
#' @examples
#' divide()

divide <- function(x){
  y <- x/7
  print("and the answer is...")
  print(y)
}

